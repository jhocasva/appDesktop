public class TiposDeDatos {
  public static void main(String[] args)  {
      // tipo de datos primitivos (toman valores por defecto)
      boolean variableBooleana = false;
      byte variableByte = 127;
      short variableShort = 300;
      char variableChar = 's';
      int variableInt = 2000000;
      long variableLong = 8765000;
      float variableFloat = 2000000.5f;
      double variableDouble = 8765000.5;

      System.out.println("valor por defecto de la variable booleana"+ variableBooleana);
      System.out.println("valor por defecto de la variable byte"+ variableByte);
      System.out.println("valor por defecto de la variable short"+ variableShort);
      System.out.println("valor por defecto de la variable char "+ variableChar);
      System.out.println("valor por defecto de la variable int"+ variableInt);
      System.out.println("valor por defecto de la variable long"+ variableLong);
      System.out.println("valor por defecto de la variable float"+ variableFloat);
      System.out.println("valor por defecto de la variable double"+ variableDouble);

      // tipo de datos >Wrapper (son por defecto nulos)
      Boolean variableBooleanaWrapper = false;
      Byte variableByteWrapper = 18;
      Short variableShortWrapper = 240;
      Character variableCharWrapper = 'j';
      String variableStringWrapper = "kjdjhdjh";
      Integer variableIntWrapper = 2000000;
      Long variableLongWrapper = 8765000l;
      Float variableFloatWrapper = 2000000.5f;
      Double variableDoubleWrapper = 8765000.5;

      System.out.println("valor por defecto de la variable booleanaWrapper"+ variableBooleanaWrapper);
      System.out.println("valor por defecto de la variable byteWrapper"+ variableByteWrapper);
      System.out.println("valor por defecto de la variable shortWrapper"+ variableShortWrapper);
      System.out.println("valor por defecto de la variable charaterWrapper "+ variableCharWrapper);
      System.out.println("valor por defecto de la variable integerWrapper"+ variableIntWrapper);
      System.out.println("valor por defecto de la variable longWrapper"+ variableLongWrapper);
      System.out.println("valor por defecto de la variable floatWrapper"+ variableFloatWrapper);
      System.out.println("valor por defecto de la variable doubleWrapper"+ variableDoubleWrapper);
      System.out.println("valor por defecto de la variable stringWrapper"+ variableStringWrapper);




  }


}
